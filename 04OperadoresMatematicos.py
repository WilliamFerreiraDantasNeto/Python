# Aula 5 - Operadores matemáticos
"""
+ soma
- subtração
* multiplicação
/ divisão
// divisão inteira
** potencia
% resto de divisão
() como na matematica
"""
print('Soma', 15+10)
print('Subtração', 15-10)
print('Multiplicação', 15*10)
print('Divisão', 15/10)
print('Divisão inteira', 15//10)
print('Divisão inteira', 10.5//3)
print('Potencia', 10**2)
print('Resto divisão', 15 % 10)
print('int * str repetição', 10*'10')  # repetindo a str
print('concatenação', '15'+'10')  # entre str apenas
# pyton não aceita concatenação entre int e str
print(9**0.5)
print(5+2*10) # ordem de prioridade multiplicação depois soma
print((5+2)*10) # entre parenteses e depois multiplicação
